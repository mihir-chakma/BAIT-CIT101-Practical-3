"""
To find the hypotenuse of a right-angled triangle given the lengths of the other two sides, 
we can use the Pythagorean theorem, which states that in a right triangle, 
the square of the length of the hypotenuse is equal to the sum of the squares of the 
lengths of the other two sides. 
"""

side1 = 3.5
side2 = 1.73

hypotenuse = (side1**2 + side2**2)**0.5

print(f"The length of the hypotenuse is {hypotenuse:.2f} cm")

